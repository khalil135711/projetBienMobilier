package models;

public class message {
	
	private int idMsg;
	private String msg;
	private String nomEm;
	public int getIdMsg() {
		return idMsg;
	}
	public void setIdMsg(int idMsg) {
		this.idMsg = idMsg;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getNomEm() {
		return nomEm;
	}
	public void setNomEm(String nomEm) {
		this.nomEm = nomEm;
	}
	
	

}
